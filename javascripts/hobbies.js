$(document).ready(function () {
  $("#data tr").on({
    mouseenter: function () {
      $("#data tr").removeClass("highlighted");
      $(this).addClass("highlighted");
    },
    mouseleave: function () {
      $("#data tr").removeClass("highlighted");
    }
  });
});
